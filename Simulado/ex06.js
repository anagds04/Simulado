/* Classificação de Nota
Considere a variável:
let nota = 6.5;

Crie uma estrutura if/else if/else que:
● Se a nota for menor que 5, exiba "Reprovado";
● Se a nota for entre 5 e 7(inclusive), exiba "Em recuperação";
● Se a nota for maior que 7, exiba "Aprovado".*/

var nota = 6.5;

if (nota < 5) {
    console.log("Reprovado!!")
}
else if (nota <= 7) {
    console.log("Recuperação!!")

} else {
    console.log("Aprovado!!")
}


