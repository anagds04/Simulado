/* Ex 12) Soma de 1 a N com For
Considere a variável:
let N = 5;

Utilize for para somar todos os números de 1 até N e exiba o resultado.*/

let n = 5;
let soma = 0;

for(let i = 1; i <= n; i++) {

    soma += i;

}

console.log(soma);



