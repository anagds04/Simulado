/*Ex 13) Contagem Regressiva com While
Considere a variável:
let numero = 5;

Utilize while para fazer a contagem regressiva até 0. */

let numero = 5;

while (numero >= 0) {
    console.log(numero);
    numero --; 
}

/*while (numero >=0) continua rodando enquanto o número for maior ou igual a ).
numero --( diminuiu o numero em 1 a cada volta) */
